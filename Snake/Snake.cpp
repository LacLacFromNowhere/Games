int main()
{
    srand(time(0));

    do
    {
        system("cls");
        loadMap();
        spawnSnake();
        createNewFood();
        displayMap();
        printf("Score: %-10dSpeed: %-10d\n", score, speed);
        printf("Press M to select map.\n");
        printf("Press W A S D to move (ESC to quit).\n");

        char direction;

        bool isSelectingMap = false;
        while (true)
        {
            if (_kbhit)
            {
                direction = _getch();
                if (direction == 27)
                {
                    return 0;
                }
                if (direction == 'm')
                {
                    selectMap();
                    isSelectingMap = true;
                    break;
                }
                if (direction == 'w' || direction == 'a' || direction == 's' || direction == 'd')
                {
                    break;
                }
            }
        }

        if (isSelectingMap)
        {
            isSelectingMap = false;
            continue;
        }

        play(direction);

        printf("Game over. Press R to restart (ESC to quit).\n");
        do
        {
            if (_kbhit)
            {
                char ch = _getch();
                if (ch == 'r')
                {
                    break;
                }
                else
                {
                    if (ch == 27)
                    {
                        return 0;
                    }
                }
            }
        } while (true);
    } while (true);
    system("cls");
}
